package day11;

import java.util.ArrayList;

public class InteratingArrayListElement {

	 public static void main(String args[]){  
	     ArrayList<String> alist=new ArrayList<String>();  
	     alist.add("Mythri");
	     alist.add("Maithri");
	     alist.add("Mythree");
	     alist.add("Maithree");
	  
	     //iterating ArrayList
	     for(String str:alist)  
	        System.out.println(str);  
	     } 

}
